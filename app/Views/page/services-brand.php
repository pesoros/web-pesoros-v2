									<h2>Company Profile</h2>

									<p>Company profile atau profil perusahaan merupakan sebuah media atau alat yang bermanfaat untuk memberikan informasi atau gambaran secara lengkap mengenail detail perusahaan. Secara umum company profile memiliki peran sebagai alat marketing yang ditujukan untuk memperoleh klien. Bisa dikatakan bahwa company profile memang wajib dimiliki oleh setiap perusahaan yang sedang beroperasi.<br><br>

									Beberapa fungsi dan manfaat company profile antara lain memuat informasi detail mengenai perusahaan, berperan sebagai media representasi dan tools marketing, pelengkap dalam proposal, serta berperan sebagai media untuk branding dan publikasi.<br><br>
									
									Sebenarnya tujuan utama dalam pembuatan company profile ialah untuk mendekati lingkup segmen pasar baru yang penting dalam pertumbuhan bisnis suatu perusahaan. Tentu company profile dapat diarahkan kepada investor potensial apabila perusahaan sedang mencari pendanaan, pelanggan baru, kerja sama, maupun memperluas bisnis.<br><br>
									
									Beberapa informasi yang harus ada dalam company profile sebuah perusahaan antara lain latar belakang perusahaan, bentuk pelayanan atau produk, dan kontak perusahaan (Alamat, nomor telepon, nomor fax, email, website, dll).<br><br>
									
									Bisa juga disisipkan dengan sejarah perusahaan, susunan perusahaan, visi dan misi, serta pengalaman atau portfolio perusahaan (Daftar klien, produk atau karya yang sudah dihasilkan, dll). Seluruh pelayanan atau produk dapat disajikan secara detail maupun intinya saja, tergantung keinginan dan fungsinya. Namun tidak sedikit sebuah perusahaan memperbarui secara berkala.<br><br>
									
									Anda juga bisa mencantumkan rencana masa depan perusahaan, baik dalam jangka pendek maupun jangka panjang. Kemudian testimonial dari klien atau customer, pernyataan motto, slogan serta motivasi perusahaan, serta sebuah panduan filosofi untuk perusahaan.<br><br>
									
									Dengan sendirinya sebuah company profile yang representatif akan menjadi ajang promosi dalam meyakinkan pihak yang akan diajak bekerjasama dengan menunjukkan kredibilitas perusahaan atau institusi tersebut.</p>
									
