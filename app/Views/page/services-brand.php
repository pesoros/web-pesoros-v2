									<h2>Branding</h2>

									<p>Sebelum mengetahui apa itu branding, Anda perlu memahami dasar katanya, brand.<br><br>

									Istilah brand sendiri pertama kali digunakan oleh orang-orang Inggris pada abad ke-19 sebagai bentuk memberi tanda kepada hewan-hewan ternak dan juga budak dengan memberi cap besi panas pada tubuh mereka.<br><br>
									
									Saat itu mereka menyebut dengan kata burn. Kemudian kata brand mulai populer digunakan oleh orang-orang jerman dengan makna yang sama yaitu menandai sesuatu menggunakan cap besi panas, brennen.<br><br>
									
									Brand sendiri saat ini diartikan sebagai identitas diri yang membedakan antar sesama baik manusia, produk, maupun tempat.<br><br>
									
									Sedangkan branding adalah sebuah kegiatan komunikasi, mempekuat, mempertahankan sebuah brand dalam rangka memberikan perspektif kepada orang lain yang melihatnya.<br><br>
									
									Menurut Kotler (2009), branding merupakan nama, istilah, tanda, simbol, rancangan atau kombinasi dari semuanya yang dimaksudkan untuk mengidentifikasi barang atau jasa atau kelompok penjual dengan untuk membedakannya dari barang atau jasa pesaing.<br><br>
									
									Sedangkan menurut Landa (2006), pengertiannya bukanlah sekedar merek atau nama dagang dari sebuah produk, jasa, atau perusahaan.<br><br>
									
									Namun semuanya yang berkaitan dengan hal-hal yang kasa mata dari sebuah merek mulai dari nama dagang, logo, ciri visual, citra, kredibilitas, karakter, kesan, persepsi, dan anggapan yang ada di benak konsumen perusahaan tersebut.<br><br>
									
									Lambat laun definisi branding semakin berkembang hingga kini didefinisikan sebagai kumpulan kegiatan komunikasi yang dilakukan perusahaan dalam rangka proses membangun dan membesarkan brand.<br><br>
									
									Bagi perusahaan hal ini bukan hanya sekedar merek atau produk Anda dikenal tetapi juga sebagai image perusahaan secara keseluruhan atau bagaimana masyarakat merasa bahwa perusahaan Anda merupakan yang terbaik.</p>

									<h2>Unsur Branding</h2>

									<ul>
										<li><strong>Nama merek</strong> - Nama adalah hal pertama yang harus dipenuhi jika kita akan melakukan branding. Tanpa sebuah nama, maka produk tidak akan memiliki identitas yang akan memudahkannya untuk dikenali masyarakat.</li><br>
										<li><strong>Logo</strong> - Dalam pembuatan logo, perhatikanlah faktor keunikan dan imej yang sesuai dengan brand. Logo yang unik akan meninggalkan kesan tak terlupakan bagi konsumen.</li><br>
										<li><strong>Tampilan visual</strong> - Tampilan visual ini bisa diaplikasikan pada desain produk, desain kemasan, desain seraga, dan lain sebagainya. Menggunakan tampilan visual dengan warna-warna yang cerah atau elegan akan menambah pencitraan terhadap sebuah produk.</li><br>
										<li><strong>Penggunaan juru bicara</strong> - Juru bicara dalam hal ini bisa jadi  seorang co-founder dari perusahaan, maskot, tokoh perusahaan, atau orang terkenal yang telah diajak bekerja sama untuk meningkatkan pemasaran produk.</li><br>
										<li><strong>Suara</strong> - Kehadiran sebuah lagu akan melengkapi unsur visualisasi dan membuatnya lebih indah dan lebih diingat.</li><br>
										<li><strong>Kata-kata </strong> - Slogan yang cerdas selalu meninggalkan kesan mendalam. Dalam proses pembuatan brand, gunakan kata- kata yang memiliki unsur ceria dan positif, mudah diingat, dan beda dari brand lain.</li>
									</ul>

